public class Human {
    String name;
    int weight;
    int salary;

    public Human(String name, int weight, int salary) {
        this.name = name;
        this.weight = weight;
        this.salary = salary;
    }

    public String getName() {
        return name;
    }

    public int getWeight() {
        return weight;
    }

    public int getSalary() {
        return salary;
    }


}
